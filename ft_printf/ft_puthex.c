#include "ft_printf.h"

int	ft_hexlower(unsigned int nb)
{
	int		i;
	char	*hex;

	hex = "0123456789abcdef";
	i = 0;
	if (nb >= 16)
		i += ft_hexlower(nb / 16);
	i += ft_putchar(hex[nb % 16]);
	return (i);
}

